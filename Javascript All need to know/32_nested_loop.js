let n = 5;
for (let i = 1; i <= n; i++) {
  let result = "";
  for (let j = 0; j <= i; j++) {
    result += j + " ";
  }
  console.log(result);
}

let ster = 5;
for (let i = 1; i <= ster; i++) {
  let result = "";

  for (let j = 1; j <= ster; j++) {
    result += "*" + " ";
  }

  console.log(result);
}
